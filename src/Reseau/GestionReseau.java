    private void JBAjouterSourceActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_JBAjouterSourceActionPerformed
        // verifier que l'arc n'a aucun precedent (origine=null)
        DialAjoutSource dial=new DialAjoutSource(this, true, R.ListeArcs);
        if(dial.valide){
            if(R.Rech_Arc(dial.cod_arc).Origine==null){
              R.Ajouter_Source(dial.cod_src, dial.periode, dial.cod_arc);
              this.LSTM.fireTableDataChanged();
            }else{
                JOptionPane.showMessageDialog(this,
                        "Une source ne peut etre créée que sur un arc extrémité !",
                        "Erreur ",
                        JOptionPane.WARNING_MESSAGE);
            }
        }
    }//GEN-LAST:event_JBAjouterSourceActionPerformed

