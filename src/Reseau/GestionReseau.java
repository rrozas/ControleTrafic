    public int Rech_coord_x(int f){
        int corx=0;
        int i=this.JTABListeCarrefours.getSelectedRow();
        if(i>=0){
       if (f== this.LCTA.LC.elementAt(i).code_carrefour)
       {
             corx= this.LCTA.LC.elementAt(i).coor_x;
             
        }
        
    }
        return corx;
    }
        public int Rech_coord_y(int f){
            int cory=0;
        int i=this.JTABListeCarrefours.getSelectedRow();
        if(i>=0){
       if (f== this.LCTA.LC.elementAt(i).code_carrefour)
       {
          
             cory= this.LCTA.LC.elementAt(i).coor_y;
        }
        
    }
        return cory;
    }
	void AjouterCarrefour(){
            
		String f = (String)JOptionPane.showInputDialog(
				   this,
                   
                        
                        
				   "Saisir le nom du carrefour � ajouter",
				   "Ajout de nouveau carrefour",
				   JOptionPane.QUESTION_MESSAGE,
				   null,
				   null, // c'est ouvert !!!
				   null); // valeur initiale
                int s= Integer.parseInt(f);
                if ((f != null) && (f.length() > 0) && (R.Rech_Carrefour(s)==null)){
                 
            	
		int cor_x= Rech_coord_x(s);
                int cor_y=Rech_coord_y(s);
			this.R.Ajouter_Carrefour(s, cor_x, cor_y);
            this.AR.AjouterAgentCarrefour(s);
			this.LCTA.fireTableDataChanged();
                }else{
            //System.out.println("le carrefour "+s+" n'est pas créé !!");
        }

	}
	void Afficher_Arc_Carrefour(){
		int index=this.JTABListeCarrefours.getSelectedRow();
		if (index>=0){
			this.LAE_Model=new ListeArcTabModel(this.R.ListeCarrefour.get(index).E, "code arc entrant");
			this.LAS_Model=new ListeArcTabModel(this.R.ListeCarrefour.get(index).S, "code arc sotrant");
			this.JTABArcsEn.setModel(LAE_Model);
			this.JTABArcsSor.setModel(LAS_Model);
		}
	}

